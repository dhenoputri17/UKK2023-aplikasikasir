    private void jum_menuKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jum_menuKeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            int harga;
            int jumlah;
            int total;
            
            harga = Integer.parseInt(harga_menu.getText());
            jumlah = Integer.parseInt(jum_menu.getText());
            total = harga*jumlah;
            
            total_menu.setText(Integer.toString(total));
        }
    }//GEN-LAST:event_jum_menuKeyReleased

