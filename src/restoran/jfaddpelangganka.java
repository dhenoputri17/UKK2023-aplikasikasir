public void cari(String key){
         pel = new DefaultTableModel();
         pel.addColumn("ID Pelanggan");
          pel.addColumn("Nama Pelanggan");
           pel.addColumn("Nama Pengguna");
            pel.addColumn("Kata Sandi");
             jTable1.setModel(pel);
             Connection conn =  koneksi.getConnection();
             try{
                  java.sql.Statement cc = conn.createStatement();
                  SQL ="select id_user, nama_user,username, password from user where nama_user like %'"+key+"'%";
                  java.sql.ResultSet a = cc.executeQuery(SQL);
                  while(a.next()){
                      pel.addRow(new Object[]{
                          a.getString("id_user"),
                          a.getString("nama_user"),
                          a.getString("username"),
                          a.getString("password")
                      });
                  }
             }catch(SQLException e){
                 System.out.println(e.getMessage());
             }
     }

    
