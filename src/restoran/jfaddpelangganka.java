    public void iduser(){
        Connection conn = koneksi.getConnection();
        try{
            java.sql.Statement ss = conn.createStatement(java.sql.ResultSet.TYPE_SCROLL_SENSITIVE, 
                    java.sql.ResultSet.CONCUR_UPDATABLE);
            SQL = "select max(right(id_user, 5)) as no from user";
            java.sql.ResultSet rr = ss.executeQuery(SQL);
            while(rr.next()){
                if(rr.first() == false) {
                            id_pelanggan.setText("RS00001");
                        } else {
                            rr.last();
                            int auto_id = rr.getInt(1) + 1;
                            String no = String.valueOf(auto_id);
                            int id = no.length();
                            
                            for(int j = 0; j < 5 - id; j++) {
                                no = "0" + no;
                            }
                            id_pelanggan.setText("RS" + no);
                        }
            }
        }catch(SQLException e) {}
    }
     
